    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        obradaRiba.setNew();
        // Za combobox castanje u zagradi ide klasa((Ribar)cmbRibar.getSelectedItem())
        obradaRiba.setPodaci(txtDatumMrjestenja.getText(), (Ribar)cmbRibar.getSelectedItem());
        try {
            obradaRiba.create();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        obradaRiba.setPodaci(txtDatumMrjestenja.getText(), (Ribar)cmbRibar.getSelectedItem()); //ovo kopiraj
        try {
            obradaRiba.update();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    if(lstEntiteti.getSelectedValue() == null){
            return;
        }
        obradaRiba.setEntitet(lstEntiteti.getSelectedValue());
        try {
            obradaRiba.delete();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }    }//GEN-LAST:event_btnDeleteActionPerformed



