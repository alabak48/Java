    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if(evt.getValueIsAdjusting() || lstEntiteti.getSelectedValue() == null){
            return;
        }
        obradaRibnjak.setEntitet(lstEntiteti.getSelectedValue());
        txtNaziv.setText(lstEntiteti.getSelectedValue().getNaziv()); 
        txtOpis.setText(lstEntiteti.getSelectedValue().getNaziv());
        
        
         DefaultListModel<Riba> rr = new DefaultListModel<>();
        if (lstEntiteti.getSelectedValue().getRiba()!= null) {
            rr.addAll(lstEntiteti.getSelectedValue().getRiba());
        }
        lstRibnjakSRibama.setModel(rr);
        lstRibnjakSRibama.repaint();
    }//GEN-LAST:event_lstEntitetiValueChanged

