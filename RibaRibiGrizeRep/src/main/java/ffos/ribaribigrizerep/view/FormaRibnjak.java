    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        obradaRibnjak.setNew();
        List<Riba> riba = new ArrayList<>();
        for(int i = 0; i < lstRibnjakSRibama.getModel().getSize(); i++) {
            riba.add(lstRibnjakSRibama.getModel().getElementAt(i));
        }
        obradaRibnjak.setPodaci(txtNaziv.getText(), txtOpis.getText(), riba);
        try {
            obradaRibnjak.create();
            ucitajPodatke();
        } catch (Exception e) {}
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (lstEntiteti.getSelectedValue() == null) {
            return;
        }
        obradaRibnjak.setEntitet(lstEntiteti.getSelectedValue());
        try {
            obradaRibnjak.delete();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());}
    
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
     if (lstEntiteti.getSelectedValue() == null) {
            return;
        }
        obradaRibnjak.setEntitet(lstEntiteti.getSelectedValue());
        List<Riba> riba = new ArrayList<>();
        for(int i = 0; i < lstRibnjakSRibama.getModel().getSize(); i++) {
            riba.add(lstRibnjakSRibama.getModel().getElementAt(i));
        }
        obradaRibnjak.setPodaci(txtNaziv.getText(), txtOpis.getText(), riba);
        try {
            obradaRibnjak.update();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());}
        
        }//GEN-LAST:event_UpdateActionPerformed

    
