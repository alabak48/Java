    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
         if(evt.getValueIsAdjusting() || lstEntiteti.getSelectedValue() == null){
            return;
        }
        obradaDijete.setEntitet(lstEntiteti.getSelectedValue());
        txtIme.setText(lstEntiteti.getSelectedValue().getIme());
        txtPrezime.setText(lstEntiteti.getSelectedValue().getPrezime());
    }//GEN-LAST:event_lstEntitetiValueChanged

    private void btnCrudCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrudCreateActionPerformed
        obradaDijete.setNew();
        obradaDijete.setPodaci(txtIme.getText(), txtPrezime.getText());
        try {
            obradaDijete.create();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnCrudCreateActionPerformed

    private void btnCrudUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrudUpdateActionPerformed
        obradaDijete.setPodaci(txtIme.getText(), txtPrezime.getText());
        try {
            obradaDijete.update();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnCrudUpdateActionPerformed

    private void btnCrudDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrudDeleteActionPerformed
        if(lstEntiteti.getSelectedValue() == null){
            return;
        }
        obradaDijete.setEntitet(lstEntiteti.getSelectedValue());
        try {
            obradaDijete.delete();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnCrudDeleteActionPerformed

 


