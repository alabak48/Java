    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if (evt.getValueIsAdjusting() || lstEntiteti.getSelectedValue() == null) {
            return;
        }
        txtIme.setText(lstEntiteti.getSelectedValue().getIme());
        txtPrezime.setText(lstEntiteti.getSelectedValue().getPrezime());
        txtDatumRodjenja.setText(lstEntiteti.getSelectedValue().getDatumRodjenja().toString());

        DefaultListModel<Skola> m = new DefaultListModel<>();
        if (lstEntiteti.getSelectedValue().getSkole() != null) {
            m.addAll(lstEntiteti.getSelectedValue().getSkole());
        }
        lstUcenikSkola.setModel(m);
        lstUcenikSkola.repaint();
    }//GEN-LAST:event_lstEntitetiValueChanged

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        obradaUcenik.setNew();
        List<Skola> skole = new ArrayList<>();
        for(int i = 0; i < lstUcenikSkola.getModel().getSize(); i++) {
            skole.add(lstUcenikSkola.getModel().getElementAt(i));
        }
        obradaUcenik.setPodaci(txtIme.getText(), txtPrezime.getText(), txtDatumRodjenja.getText(), skole);
        try {
            obradaUcenik.create();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (lstEntiteti.getSelectedValue() == null) {
            return;
        }
        obradaUcenik.setEntitet(lstEntiteti.getSelectedValue());
        List<Skola> skole = new ArrayList<>();
        for(int i = 0; i < lstUcenikSkola.getModel().getSize(); i++) {
            skole.add(lstUcenikSkola.getModel().getElementAt(i));
        }
        obradaUcenik.setPodaci(txtDatumRodjenja.getText(), txtIme.getText(), txtPrezime.getText(), skole);
        try {
            obradaUcenik.update();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (lstEntiteti.getSelectedValue() == null) {
            return;
        }
        obradaUcenik.setEntitet(lstEntiteti.getSelectedValue());
        try {
            obradaUcenik.delete();
            ucitajPodatke();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

