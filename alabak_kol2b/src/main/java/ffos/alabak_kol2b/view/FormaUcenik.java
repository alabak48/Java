    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        DefaultListModel<Skola> us;

        if (lstEntiteti.getSelectedValue().getSkole() != null) {
            us = (DefaultListModel<Skola>) lstUcenikSkola.getModel();
        } else {
            us = new DefaultListModel<>();
            lstUcenikSkola.setModel(us);
        }

        if (lstEntiteti.getSelectedValue().getSkole() == null) {
            lstEntiteti.getSelectedValue().setSkole(new ArrayList<>());
        }
        for (Skola s : lstSkole.getSelectedValuesList()) {
            if (!ucenikUSkoli(us, s)) {
                us.addElement(s);
            }
        }
        lstUcenikSkola.repaint();
        lstSkole.repaint();
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        DefaultListModel<Skola> us = (DefaultListModel<Skola>) lstUcenikSkola.getModel();
        for (Skola s : lstUcenikSkola.getSelectedValuesList()) {
            us.removeElement(s);
        }

        lstUcenikSkola.repaint();
        lstSkole.repaint();
    }//GEN-LAST:event_btnObrisiActionPerformed
    private boolean ucenikUSkoli(DefaultListModel<Skola> sk, Skola s) {
        for (int i = 0; i < sk.size(); i++) {
            if (sk.get(i).getSifra() == s.getSifra()) {
                return true;
            }
        }
        return false;
    }


